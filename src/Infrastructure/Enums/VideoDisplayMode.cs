namespace Softeq.ImagePicker.Infrastructure.Enums
{
    public enum VideoDisplayMode
    {
        /// <summary>
        /// Preserve aspect ratio, fit within layer bounds.
        /// </summary>
        AspectFit,

        /// <summary>
        /// Preserve aspect ratio, fill view bounds.
        /// </summary>
        AspectFill,

        /// <summary>
        /// Stretch to fill layer bounds
        /// </summary>
        Resize
    }
}